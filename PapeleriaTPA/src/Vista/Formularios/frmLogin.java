    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        String user = txtUser.getText();
        String password = new String(txtPassword.getPassword());
        if (user.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar un usuario");
            return;
        }

        if (password.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar una clave");
            return;
        }
        LogicaDeNegocio ln = new LogicaDeNegocio();
        if (!ln.validarLoginUsuario(user, password)) {
            JOptionPane.showMessageDialog(this, "Usuaio o clave no valida");
            txtUser.setText("");
            txtPassword.setText("");
            txtUser.requestFocusInWindow();
            return;
        }
        usuarioActual = txtUser.getText();
//        ln.cerrarcon();
        //Formularios.FrmPrincipal principal = new Formularios.FrmPrincipal();
        this.setVisible(false);
        //principal.setVisible(true);

    }//GEN-LAST:event_btnAceptarActionPerformed

